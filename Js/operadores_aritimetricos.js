var num1 = prompt('Infome o primeiro número:');

var num2 = prompt('Infome o segundo número:');

document.write('A soma dos valores é:' + (num1 + num2) + '<br />'); 

document.write('A subtração dos valores é:' + (num1 - num2)  + '<br />');
document.write('A divição dos valores é:' + (num1 / num2)  + '<br />');
document.write('A multiplicação dos valores é:' + (num1 * num2)  + '<br />');
document.write('O módulo dos valores é:' + (num1 % num2)  + '<br />');
document.write('O incrimento do primeiro valor é:' + (++num1) + '<br />');
document.write('O decremento do segundo valor é:' + (--num2) + '<br />');