// propriedade length: retorna a quantidade de caracteres de string;
// lembrando que as strings sempre começam pelo valor 0;

document.write('Jéssica'.length)

document.write('</br>');


// propriedade charAt(): é passado um indice e a função retorna a string representada pelo indice;
document.write('Jéssica'.charAt(6))

document.write('</br>');

// propriedade indexOf(): retorna a posição da primeira ocorrênia específica do valor dentro da string;

document.write('Jéssica'.indexOf('a'));

//propriedade reclace: ele pesquisa uma cadeia de caracteres identica passada no parâmetro e substitui 
// pela string ou cadeia de caracteres desejado passada tbm como parâmetro;

document.write('</br>');

document.write('Jéssica Talita'.replace('Talita', ' é da família Cruz'));